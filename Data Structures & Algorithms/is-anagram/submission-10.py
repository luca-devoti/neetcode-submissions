class Solution:
    def isAnagram(self, s: str, t: str) -> bool:
        alpha1 = {
            97: 0, 98: 0, 99: 0,
            100: 0, 101: 0, 102: 0,
            103: 0, 104: 0, 105: 0,
            106: 0, 107: 0, 108: 0,
            109: 0, 110: 0, 111: 0,
            112: 0, 113: 0, 114: 0,
            115: 0, 116: 0, 117: 0,
            118: 0, 119: 0, 120: 0,
            121: 0, 122: 0
        }

        alpha2 = {
            97: 0, 98: 0, 99: 0,
            100: 0, 101: 0, 102: 0,
            103: 0, 104: 0, 105: 0,
            106: 0, 107: 0, 108: 0,
            109: 0, 110: 0, 111: 0,
            112: 0, 113: 0, 114: 0,
            115: 0, 116: 0, 117: 0,
            118: 0, 119: 0, 120: 0,
            121: 0, 122: 0
        }

        for c in s:
            alpha1[ord(c)] += 1
        for c in t:
            alpha2[ord(c)] += 1
        for i in range(97, 123):
            if alpha1[i] != alpha2[i]:
                return False
        return True